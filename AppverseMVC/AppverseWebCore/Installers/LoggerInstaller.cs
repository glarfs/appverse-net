using Castle.Facilities.Logging;
using Castle.MicroKernel.Registration;
using Castle.MicroKernel.SubSystems.Configuration;
using Castle.Windsor;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;

namespace Appverse.Web.Core.Installers
{
    /// <summary>
    /// Installer that adds the LoggingFacility facility to the container. We configure it indicating that we want to use the Log4net api.
    /// After this, to log any message, we only need to declare "public ILogger Logger { get; set; }". Very important: Use: Castle.Core.Logging.ILogger, not log4net.Core.ILogger
    /// </summary>
    public class LoggerInstaller : IWindsorInstaller
    {
        /// <summary>
        /// Performs the installation in the <see cref="T:Castle.Windsor.IWindsorContainer" />.
        /// </summary>
        /// <param name="container">The container.</param>
        /// <param name="store">The configuration store.</param>
        public void Install(IWindsorContainer container, IConfigurationStore store)
        {
            container.AddFacility<LoggingFacility>(f => f.UseLog4Net());
        }
    }
}